import UIKit
import Material

extension UIColor {
    
    public class var primary: UIColor {
        return UIColor(red: 224, green: 30, blue: 105, alpha: 1)
    }
    
    public class var grayDark: UIColor {
        return UIColor(red: 102.0/255, green: 102.0/255, blue: 102.0/255, alpha: 1)
    }
    
    public class var grayLight: UIColor {
        return UIColor(red: 245.0/255, green: 245.0/255, blue: 245.0/255, alpha: 1)
    }
}
